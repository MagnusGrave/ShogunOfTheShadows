package com.coreyfarmer.shogunoftheshadows;

public class Size {
    public Size(int width, int height) {
        this.width = width;
        this.height = height;
    }

    private int width;
    public int getWidth() {
        return width;
    }
    public int height;
    public int getHeight() {
        return height;
    }
}
